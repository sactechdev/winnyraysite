-- 3. PROPERTIES TABLE (Future Use)
-- For listing houses, apartments, and land.
CREATE TABLE IF NOT EXISTS properties (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT now(),
  title TEXT NOT NULL,
  description TEXT,
  price NUMERIC,
  location TEXT NOT NULL,
  category TEXT NOT NULL, -- sale, rent, lease
  type TEXT NOT NULL, -- house, apartment, land
  beds INTEGER,
  baths INTEGER,
  sqft NUMERIC,
  images TEXT[] DEFAULT '{}',
  features TEXT[] DEFAULT '{}',
  is_featured BOOLEAN DEFAULT false,
  status TEXT DEFAULT 'available' -- available, sold, rented
);

-- 4. USER PROFILES
-- Extends Supabase Auth with custom roles.
CREATE TABLE IF NOT EXISTS profiles (
  id UUID REFERENCES auth.users ON DELETE CASCADE PRIMARY KEY,
  full_name TEXT,
  role TEXT DEFAULT 'client', -- client, admin
  phone TEXT,
  created_at TIMESTAMPTZ DEFAULT now()
);

