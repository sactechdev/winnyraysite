-- WinnyRay Nigeria Limited - Supabase Schema
-- This schema covers bookings, site content management, and future property listings.

-- 1. BOOKINGS & ENQUIRIES TABLE
-- Handles cleaning requests, quote requests, and real estate enquiries.
CREATE TABLE IF NOT EXISTS bookings (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT now(),
  client_name TEXT NOT NULL,
  client_email TEXT,
  client_phone TEXT NOT NULL,
  service_type TEXT NOT NULL, -- Format: "cleaning: Service Name" or "real-estate: Enquiry Type"
  address TEXT,
  city TEXT DEFAULT 'Kano',
  instructions TEXT,
  status TEXT DEFAULT 'pending', -- pending, contacted, completed, cancelled
  metadata JSONB DEFAULT '{}'::jsonb
);

